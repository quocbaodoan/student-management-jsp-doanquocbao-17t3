package model;

public class Schedule {
	private String mssv;
	private String tenmonhoc;
	private String sotinchi;
	private String giangvien;
	private String thoikhoabieu;
	private String tuanhoc;
	private String mamonhoc;
	public String getMssv() {
		return mssv;
	}
	public void setMssv(String mssv) {
		this.mssv = mssv;
	}
	public String getTenmonhoc() {
		return tenmonhoc;
	}
	public void setTenmonhoc(String tenmonhoc) {
		this.tenmonhoc = tenmonhoc;
	}
	public String getSotinchi() {
		return sotinchi;
	}
	public void setSotinchi(String sotinchi) {
		this.sotinchi = sotinchi;
	}
	public String getGiangvien() {
		return giangvien;
	}
	public void setGiangvien(String giangvien) {
		this.giangvien = giangvien;
	}
	public String getThoikhoabieu() {
		return thoikhoabieu;
	}
	public void setThoikhoabieu(String thoikhoabieu) {
		this.thoikhoabieu = thoikhoabieu;
	}
	public String getTuanhoc() {
		return tuanhoc;
	}
	public void setTuanhoc(String tuanhoc) {
		this.tuanhoc = tuanhoc;
	}
	public String getMamonhoc() {
		return mamonhoc;
	}
	public void setMamonhoc(String mamonhoc) {
		this.mamonhoc = mamonhoc;
	}
}
